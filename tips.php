        <h2>Safety tips</h2>
        <ul>
            <li>Never reveal your location in social media</li>
            <li>Always be careful talking to strangers</li>
            <li>Buy an Apple AirTag (or something similar) for luggage or personal belongings to keep them safe.</li>
            <li>Install translation apps on your phone/tablet.</li>
        </ul>
        <h2>Banking tips</h2>
        <ul>
            <li>Be careful while using ATM machines</li>
            <li>Check for cameras, loose keypads or loose card insert area in ATMs. Those are usually card and PIN hijackers.</li>
            <li>Choose CONTINUE WITHOUT CONVERSION when withdrawing in ATMs, because ATMs have high conversion fees (around 9% markup compared to banks).</li>
            <li>Choose to pay by CARD instead of withdrawing money from the ATM, as you will save a lot of money.</li>
            <li>Regularly check conversion rates.</li>
            <li>When possible use Contactless ATMs.</li>
        </ul>
        <h2>Internet Tips</h2>
        <ul>
            <li>Use VPNs (if you are using a roaming plan from your home carrier, it might be safe without it)</li>
            <li>Buy eSIM online or get local SIM from the country you are going to, to avoid fees or overpriced services </li>
        </ul>
